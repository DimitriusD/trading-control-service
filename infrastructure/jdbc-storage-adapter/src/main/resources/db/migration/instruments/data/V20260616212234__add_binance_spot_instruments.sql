    )
         JOIN exchanges e
              ON e.code = 'BINANCE'
         JOIN market_types mt
              ON mt.code = 'SPOT'
         JOIN exchange_markets em
              ON em.exchange_id = e.id
                  AND em.market_type_id = mt.id
         JOIN assets base_asset
              ON base_asset.code = instrument_data.base_asset_code
         JOIN assets quote_asset
              ON quote_asset.code = instrument_data.quote_asset_code ON CONFLICT (instrument_id) DO
UPDATE SET
    exchange_market_id = EXCLUDED.exchange_market_id,
    base_asset_id = EXCLUDED.base_asset_id,
    quote_asset_id = EXCLUDED.quote_asset_id,
    exchange_symbol = EXCLUDED.exchange_symbol,
